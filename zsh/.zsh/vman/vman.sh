# man 函数
# 在Vim 中查看Manual
#
#             By 秦凡东
# ==============================================================================
function vman {
  local section=''

  if [[ $# < 1 ]]; then
    exit 0
  fi

  for key in $@; do
    if echo $key | grep -P '^[1-9]$' >/dev/null 2>&1; then
      section=$key
    else
      vim -c "Man $section $key" -c 'only'
    fi

  done
}

